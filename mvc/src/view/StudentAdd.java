    int search;
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        DefaultTableModel Df = (DefaultTableModel)jTable1.getModel();
        int selectedItem = jTable1.getSelectedRow();
        
        search = Integer.parseInt(Df.getValueAt(selectedItem, 0).toString());
        StudentDAOimpl dao=new StudentDAOimpl();
        Student st=dao.get(search);
        
        jNom.setText(st.getNom());
        jPostnom.setText(st.getPostnom());
        jPrenom.setText(st.getPrenom());
        jNationalite.setText(st.getNationalite());
        jAge.setText(Integer.toString(st.age));
        
    }//GEN-LAST:event_jTable1MouseClicked

