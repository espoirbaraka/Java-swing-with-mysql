     public void Load(){
        StudentDAOimpl dao = new StudentDAOimpl();
        List<Student> list=dao.list();
        DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
        DFT.setRowCount(0);
        for(Student st: list){
            int sid=st.getId();
            String sname=st.getNom();
            String spostnom=st.getPostnom();
            String sprenom=st.getPrenom();
            String snationation=st.getNationalite();
            int sage=st.age;
            DFT.addRow(new Object[]{sid, sname, spostnom, sprenom, snationation, sage});
        }
    }

